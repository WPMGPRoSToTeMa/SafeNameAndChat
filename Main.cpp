string SafePositionalPrintf(string format, vector<string> args, bool positional = true) {
	string result{};
	enum class State {
		None,
		FoundPercent,
		FoundString,
	} state = State::None;
	size_t curArg = 0;
	for (auto ch : format) {
		switch (state) {
		case State::None: {
			if (ch == '%') {
				state = State::FoundPercent;
			} else {
				result.push_back(ch);
				state = State::None;
			}
		}
		break;
		case State::FoundPercent: {
			if (ch == 's') {
				state = State::FoundString;
			} else {
				result.push_back('%');
				if (ch != '%') {
					result.push_back(ch);
				}
				state = State::None;
			}
		}
		break;
		case State::FoundString: {
			if ('1' <= ch && ch <= '9' && ch - '1' < args.size() && positional) {
				result += args[ch - '1'];
				state = State::None;
			} else if (curArg < args.size()) {
				result += args[curArg++];
				result.push_back(ch);
				state = State::None;
			} else {
				result.push_back('%');
				result.push_back('s');
				result.push_back(ch);
				state = State::None;
			}
		}
		break;
		}
	}
	if (state == State::FoundPercent) {
		result.push_back('%');
	} else if (state == State::FoundString) {
		if (curArg < args.size()) {
			result += args[curArg++];
		} else {
			result.push_back('%');
			result.push_back('s');
		}
	}
	return result;
}

string FitUtf8_Helper(string str, size_t maxByteCount) {
	if (str.length() <= maxByteCount)
		return str;

	str.resize(maxByteCount + 1);

	/*bool addN = false;
	if (str[str.length() - 1] == '\n') {
		str.pop_back();
		addN = true;
	}*/

	char last = str[str.length() - 1];
	str.pop_back();
	if (((unsigned char)last & (0x80 | 0x40)) == 0x80) {
		while (true) {
			char ch = str[str.length() - 1];
			if (((unsigned char)ch & 0x80) == 0) {
				break;
			}

			str.pop_back();

			if (((unsigned char)ch & (0x80 | 0x40)) == (0x80 | 0x40)) {
				break;
			}
		}
	}

	/*if (addN) {
		str.push_back('\n');
	}*/

	return str;
}

string FitUtf8(string str, size_t maxByteCount) {
	str = FitUtf8_Helper(str, maxByteCount);

	if (str[str.length() - 1] != '\n') {
		str = FitUtf8_Helper(str, maxByteCount - 1);
		str.push_back('\n');
	}

	return str;
}

